#include<stdio.h>
int main()
{
    int add,mul,div,sub,mod;
    add =5+2; //addition
    sub =3-5; //subtraction
    mul = 3*5; //multiplication
    div = 7/5; //gives 1 quotient
    mod = 8%6; //gives 2 remainder
    printf("%d ,%d,%d,%d,%d",add,sub,mul,div,mod); //prints 7,-2,15,1,2
    return 0;
    
}